        private static string[] NumberOptions(int answer)
        {
            return new[] { answer.ToString(), (answer + 1).ToString(), Math.Max(0, answer - 1).ToString(), (answer + 3).ToString() }
                .Distinct()
                .ToArray();
        }

        private static void AddLevel(AppDbContext context, Subject subject, int grade, int order, string title, string description, params Exercise[] exercises)
        {
            var alreadyExists = context.ChangeTracker.Entries<LearningLevel>().Any(entry =>
                (entry.Entity.SubjectId == subject.Id || ReferenceEquals(entry.Entity.Subject, subject)) &&
                entry.Entity.Grade == grade &&
                entry.Entity.Order == order) ||
                context.LearningLevels.Any(level =>
                level.SubjectId == subject.Id &&
                level.Grade == grade &&
                level.Order == order);

            if (alreadyExists)
            {
                return;
            }

            var orderedExercises = exercises.ToList();
            for (var i = 0; i < orderedExercises.Count; i++)
            {
                orderedExercises[i].SortOrder = i + 1;
            }

            var level = new LearningLevel
            {
                Subject = subject,
                Grade = grade,
                Order = order,
                Title = title,
                Description = description,
                XpReward = 15,
                SourceTitle = OfficialSourceTitle,
                SourceUrl = OfficialSourceUrl,
                Exercises = orderedExercises
            };

            context.LearningLevels.Add(level);
        }

        private static Exercise Exercise(string prompt, IEnumerable<string> options, string correctAnswer, string explanation)
        {
            return new Exercise
            {
                Type = "single_choice",
                Prompt = prompt,
                OptionsJson = JsonSerializer.Serialize(options),
                CorrectAnswer = correctAnswer,
                Explanation = explanation,
                SortOrder = 1,
                XpReward = 5
            };
        }

        private static async Task SeedAdminAsync(AppDbContext context)
        {
            var admin = await context.Users.FirstOrDefaultAsync(u => u.Email == AdminEmail);
            if (admin == null)
            {
                admin = new User
                {
                    Email = AdminEmail,
                    Name = "Learnix Administrator",
                    PasswordHash = BCrypt.Net.BCrypt.HashPassword(AdminPassword),
                    Role = "Admin",
                    DailyGoalMinutes = 10,
                    IsActive = true
                };

                context.Users.Add(admin);
            }
            else
            {
                admin.Role = "Admin";
                admin.IsActive = true;
            }

            await context.SaveChangesAsync();
        }
