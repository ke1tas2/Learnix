        private static void SeedTemplateLevels(AppDbContext context, Dictionary<string, Subject> subjects)
        {
            foreach (var subject in subjects.Values.OrderBy(s => s.SortOrder))
            {
                for (var grade = 7; grade <= 11; grade++)
                {
                    for (var order = 1; order <= 3; order++)
                    {
                        var theme = GetTemplateTheme(subject.Code, grade, order);
                        AddLevel(
                            context,
                            subject,
                            grade,
                            order,
                            theme.Title,
                            theme.Description,
                            CreateTemplateExercises(subject.Code, grade, order));
                    }
                }
            }
        }

